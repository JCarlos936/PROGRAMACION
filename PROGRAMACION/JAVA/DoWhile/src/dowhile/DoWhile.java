public class DoWhile {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        
        int i;//Declaramos la variable
        i=0;//Inicializamos el valor de la variable
        do{
            System.out.println(i); //Le damos las intrucciones al programa
            i++; //Iterador que ir√° aumentando su valor de un en uno
        }while(i<10);//Condicionador del programa
        
        
        
        
    }
    
}
